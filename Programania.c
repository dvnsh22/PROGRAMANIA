int Bsearch(int arr[],int size,int item)
{
    int beg=0,last,mid;
    last=size-1;
    while(beg<=last)
    {
        mid=(beg+last)/2;
        if(item==arr[mid])
        {
            return mid;
        }
        else if(item>arr[mid])
        {
            beg=mid+1;
        }
        else
        {
            last=mid-1;
        }
    }
    return -1;
}
